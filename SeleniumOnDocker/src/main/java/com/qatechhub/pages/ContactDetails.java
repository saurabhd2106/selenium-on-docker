package com.qatechhub.pages;

public class ContactDetails {

	private String firstName;
	private String lastName;

	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getCourseInterestedIn() {
		return courseInterestedIn;
	}
	public void setCourseInterestedIn(String courseInterestedIn) {
		this.courseInterestedIn = courseInterestedIn;
	}
	private String emailId;
	private String phoneNumber;
	private String gender;
	private String courseInterestedIn;
	
	

}
